char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	if (str[i] > 'a' &&  str[i] < 'z')
		str[i] -= 'a' + 'A';
	i++;
	while (str[i])
	{
		if (str[i] > 'a' &&  str[i] < 'z')
		{
			if (str[i - 1] == ' ' || str[i - 1] == '-' || str[i] == '+')
				str[i] -= 'a' + 'A';
			else
				str[i] -= 'A' + 'a';
		}
		i++;
	}
	return (str);
}
