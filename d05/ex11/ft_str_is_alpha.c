int		ft_str_is_alpha(char *str)
{
	int bl_alpha;

	bl_alpha = 1;
	while (*str)
	{
		if (!('A' < *str && *str < 'Z'
					|| 'a' < *str && *str < 'z'
					|| '0' < *str && *str < '9'))
			bl_alpha = 0;
			str++;
	}
	return bl_alpha;
}
